package com.example.lab8.Beans;

public class Enemigo {
    private int idEnemigo;
    private String nombre;
    private String clase;
    private int idClase;
    private int ataque;
    private int experiencia;
    private String objetoDado;
    private float probObjeto;
    private String genero;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getClase() {
        return clase;
    }

    public void setClase(String clase) {
        this.clase = clase;
    }

    public int getAtaque() {
        return ataque;
    }

    public void setAtaque(int ataque) {
        this.ataque = ataque;
    }

    public int getExperiencia() {
        return experiencia;
    }

    public void setExperiencia(int experiencia) {
        this.experiencia = experiencia;
    }

    public String getObjetoDado() {
        return objetoDado;
    }

    public void setObjetoDado(String objetoDado) {
        this.objetoDado = objetoDado;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public float getProbObjeto() {
        return probObjeto;
    }

    public void setProbObjeto(float probObjeto) {
        this.probObjeto = probObjeto;
    }

    public int getIdEnemigo() {
        return idEnemigo;
    }

    public void setIdEnemigo(int idEnemigo) {
        this.idEnemigo = idEnemigo;
    }

    public int getIdClase() {
        return idClase;
    }

    public void setIdClase(int idClase) {
        this.idClase = idClase;
    }
}
