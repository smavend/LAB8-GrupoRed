package com.example.lab8.Beans;

public class Estadistica {
    private String clase;
    private float porcClase;
    private String objeto;
    private float porcObjeto;
    private float porcGenero;

    public String getClase() {
        return clase;
    }

    public void setClase(String clase) {
        this.clase = clase;
    }

    public float getPorcClase() {
        return porcClase;
    }

    public void setPorcClase(float porcClase) {
        this.porcClase = porcClase;
    }

    public String getObjeto() {
        return objeto;
    }

    public void setObjeto(String objeto) {
        this.objeto = objeto;
    }

    public float getPorcObjeto() {
        return porcObjeto;
    }

    public void setPorcObjeto(float porcObjeto) {
        this.porcObjeto = porcObjeto;
    }

    public float getPorcGenero() {
        return porcGenero;
    }

    public void setPorcGenero(float porcGenero) {
        this.porcGenero = porcGenero;
    }
}
