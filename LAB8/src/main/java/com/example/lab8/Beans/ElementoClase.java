package com.example.lab8.Beans;

public class ElementoClase {
    private int idElemento;
    private float porcDano;
    private String fortDeb;

    public int getIdElemento() {
        return idElemento;
    }

    public void setIdElemento(int idElemento) {
        this.idElemento = idElemento;
    }

    public float getPorcDano() {
        return porcDano;
    }

    public void setPorcDano(float porcDano) {
        this.porcDano = porcDano;
    }

    public String getFortDeb() {
        return fortDeb;
    }

    public void setFortDeb(String fortDeb) {
        this.fortDeb = fortDeb;
    }
}
